import React, { useReducer } from 'react';

const initState = {
  count: 1
}
const reducer = (state, actions) => {
  const { type, data } = actions;
  switch(type) {
    case 'increment':
      return {
        count: state.count + data
      }
    case 'decrement':
      return {
        count: state.count - data
      }
    default:
      return state
  }
}

export default function HookReducer() {
  const [state, dispatch] = useReducer(reducer, initState);
  return (
    <div>
      当前的值是----- {state.count}
      <button onClick={() => dispatch({ type: 'increment', data: 1 })}>+1</button>
      <button onClick={() => dispatch({ type: 'decrement', data: 2 })}>-2</button>
    </div>
  )
}
